
import java.util.*;
public class Copystr {
    public static void main(String args[]) {
        String strcopy;
        Scanner sc = new Scanner(System.in);
        System.out.println("Write the string");
        String str = sc.nextLine();
        strcopy = String.valueOf(str);
        System.out.println("\n string is:" + str);
        System.out.println("copy is:" + strcopy);

    }
}

